#=
Function which takes in a function and the various parameters required to run a PSO operation, creates a swarm and updates its values for the number of iterations provided. Then goes on to return the best particle in terms of accuracy of it.
=#


function PsoRun(fitFunc::Function, nDim::Int; nParticle=100, nInter::Int=4000)
    swarm = Swarm(fitFunc, nDim, nParticle=nParticle, nInter=nInter)
    initSwarm(swarm)
    
    for i in swarm.nInter
        updateSwarm(swarm)
    end
    return swarm.gBest
end


